#include <unistd.h>

int		main(void)
{
	int i;
	int b;

	i = 66;
	b = 97;
	while (b <= 122)
	{
		write(1, &b, 1);
		write(1, &i, 1);
		i = i + 2;
		b = b + 2;
	}
	write(1, "\n", 1);
}
