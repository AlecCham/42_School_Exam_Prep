#include <stdio.h>

int	ft_strlen(char *str)
{
	int i;

	while (str[i] != '\0')
		i++;
	return (i);
}

char	*ft_strrev(char *str)
{
	int		count;
	int		i;
	char	c;

	count = 0;
	while (str[count] != '\0')
		count++;
	count = count - 1;
	i = 0;
	while (i < ((count + 1) / 2))
	{
		c = str[i];
		str[i] = str[count - i];
		str[count - i] = c;
		i++;
	}
	return (str);
}

int		main(void)
{
	char str[] = "12345";
	printf("%s \n", ft_strrev(str));
}
